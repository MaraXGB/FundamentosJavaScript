// Operadores comparacion
// == igual a valor
// === igual en valor y tipo
// != deferente a 
// !== diferente de valor y tipo
// > mayor a
// < menor a
// >= mayor o igual
// <= menor o igual
const a = 10;
const b = 20;
const c = "10";

console.log(a == c); // true
console.log(a == b); // false
console.log(a === c); //false
console.log(a != c); // false
console.log(a != b); // true
console.log(a !== c); //true